// build-cookie-engine.mjs — bundles the vendored Consent-O-Matic rule engine
// into a single classic content-script file for Sieve's cookie auto-reject.
//
// What this does:
//   Takes src/cookie-engine.entry.js (which imports the verbatim engine under
//   vendor/consent-o-matic/Extension/) and flattens the whole ES-module graph
//   into one self-contained IIFE: content/cookie-engine.bundle.js. That file
//   assigns window.SieveCookieEngine and is safe to load as a normal MV3
//   content script (which cannot itself be an ES module).
//
// Why it's separate from build.mjs:
//   build.mjs builds the optional TensorFlow.js toxicity bundle and is wired to
//   `npm run build`. Keeping the cookie engine in its own script means neither
//   build touches the other, and updating the engine from upstream is just:
//     (1) re-copy the 7 vendored files, (2) run `node build-cookie-engine.mjs`.
//
// This is NOT minified on purpose: the engine is small (~70 KB) and keeping it
// readable makes the vendored code auditable and easier to debug in Step 6.
//
// It also compiles the vendored rule database into a single local file
// (data/cookie-rules.json) so the extension never depends on a live third-party
// fetch at runtime — see compileRules() below.
//
// Usage:
//   node build-cookie-engine.mjs
//   node build-cookie-engine.mjs --watch

import * as esbuild from "esbuild";
import fs from "node:fs";
import path from "node:path";

/** @type {import("esbuild").BuildOptions} */
const options = {
  entryPoints: ["src/cookie-engine.entry.js"],
  outfile: "content/cookie-engine.bundle.js",
  bundle: true,
  format: "iife", // a classic browser script, not an ES module
  platform: "browser",
  target: ["chrome109", "firefox109"],
  minify: false,
  legalComments: "inline",
  banner: {
    js:
      "/* Sieve cookie auto-reject engine — bundled from vendor/consent-o-matic\n" +
      "   (Consent-O-Matic, MIT License, (c) CAVI / Aarhus University).\n" +
      "   Generated by build-cookie-engine.mjs — do not edit by hand.\n" +
      "   Update procedure: vendor/consent-o-matic/ATTRIBUTION.md */",
  },
  logLevel: "info",
};

// ---------------------------------------------------------------------------
// Rule database compilation.
//
// Consent-O-Matic ships one JSON file per CMP plus an index (rules-list.json)
// listing which files are "active" and in what order. At runtime their engine
// wants ONE merged config object of the shape { cmpName: ruleConfig, ... }.
//
// We reproduce that merge here, at build time, from the LOCAL vendored copy —
// so Sieve packages a single ready-to-load file and never fetches from GitHub:
//   1. Read vendor/consent-o-matic/rules-list.json (the authoritative active set).
//   2. Load each referenced rule file, in list order, de-duplicating repeats.
//   3. Strip each file's "$schema" key (metadata the engine ignores).
//   4. Object.assign them together — later files override earlier on key clashes,
//      exactly as upstream's engine does (e.g. trustarc_frame_2022 wins).
//   5. Write minified data/cookie-rules.json.
//
// Any rule file present in vendor/rules/ but NOT listed in rules-list.json is
// intentionally skipped, matching upstream (e.g. onetrust_hidden.json).
// ---------------------------------------------------------------------------
function compileRules() {
  const vendorDir = "vendor/consent-o-matic";
  const rulesDir = path.join(vendorDir, "rules");
  const outFile = "data/cookie-rules.json";

  const list = JSON.parse(fs.readFileSync(path.join(vendorDir, "rules-list.json"), "utf8"));
  const refNames = list.references
    .map((url) => url.split("/rules/")[1])
    .filter(Boolean);

  const seen = new Set();
  const merged = {};
  let filesUsed = 0;
  let collisions = 0;

  for (const fileName of refNames) {
    if (seen.has(fileName)) continue; // de-duplicate repeated references
    seen.add(fileName);

    const filePath = path.join(rulesDir, fileName);
    if (!fs.existsSync(filePath)) {
      throw new Error(`rules-list.json references a missing rule file: ${fileName}`);
    }

    const obj = JSON.parse(fs.readFileSync(filePath, "utf8"));
    delete obj["$schema"];

    for (const key of Object.keys(obj)) {
      if (Object.prototype.hasOwnProperty.call(merged, key)) collisions++;
      merged[key] = obj[key];
    }
    filesUsed++;
  }

  // Sieve-authored supplemental rules — CMPs upstream doesn't cover yet (e.g.
  // CookieYes). Merged AFTER the vendored rules so they add new CMPs and win on
  // any key collision. These live outside vendor/ because they are ours to edit.
  const extraDir = "cookie-rules-extra";
  let extraCmps = 0;
  if (fs.existsSync(extraDir)) {
    const extraFiles = fs.readdirSync(extraDir).filter((f) => f.endsWith(".json")).sort();
    for (const fileName of extraFiles) {
      const obj = JSON.parse(fs.readFileSync(path.join(extraDir, fileName), "utf8"));
      delete obj["$schema"];
      for (const key of Object.keys(obj)) {
        merged[key] = obj[key];
        extraCmps++;
      }
    }
  }

  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, JSON.stringify(merged));

  const sizeKb = (fs.statSync(outFile).size / 1024).toFixed(1);
  console.log(
    `[Sieve] Rule DB compiled → ${outFile} ` +
      `(${filesUsed} upstream files + ${extraCmps} Sieve-authored, ` +
      `${Object.keys(merged).length} CMPs total, ${collisions} override(s), ${sizeKb} kB)`
  );
}

if (process.argv.includes("--watch")) {
  compileRules();
  const ctx = await esbuild.context(options);
  await ctx.watch();
  console.log("[Sieve] esbuild is watching the cookie engine for changes…");
} else {
  compileRules();
  await esbuild.build(options);
  console.log("[Sieve] Cookie engine build complete → content/cookie-engine.bundle.js");
}
